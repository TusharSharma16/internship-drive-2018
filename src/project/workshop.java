        if (s==0){
JOptionPane.showMessageDialog(null,"sorry! No Seats Left...");
}else
    
    try (
            Connection con = DriverManager.getConnection
                ("jdbc:mysql://localhost:3306/pix_craft", "root", "a")) {
        int x=Integer.parseInt(d.getText());
        int f =x-1;
        String d1 = (String)date1.getSelectedItem();
        String m = (String)month1.getSelectedItem();
        String y = (String)year1.getSelectedItem();
        String z=c.getText();
        Statement stmt = con.createStatement();
        String query1 = "UPDATE workshop set Seats_Left="+f+" WHERE CITY='"+z+"' && DATE='"+y+"-"+m+"-"+d1+"';";
        stmt.executeUpdate(query1);
        JOptionPane.showMessageDialog(null, "Registered"+ " Succesfully!");
        this.dispose();
        workshop form = new workshop();
        form.setVisible(true);
        stmt.close();
    
        
}
